    public void addFechaHora(){
        generarFechaAleatoria();
        generarHoraAleatoria();  
    }

    private void generarFechaAleatoria() {
        Random random = new Random();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
        Calendar fecha = Calendar.getInstance();
        fecha.set(2023, 5, random.nextInt(22));        
        for (int i = 0; i < 7; i++) {
            cbFecha.addItem(formatoFecha.format(fecha.getTime()));
            fecha.add(Calendar.DAY_OF_MONTH, 1);
        }
    }

    private void generarHoraAleatoria() {
        SimpleDateFormat formatoHora = new SimpleDateFormat("h:mm a");
        Calendar hora = Calendar.getInstance();
        hora.set(Calendar.HOUR_OF_DAY, 8);
        hora.set(Calendar.MINUTE, 0);
        hora.set(Calendar.SECOND, 0);        
        while (hora.get(Calendar.HOUR_OF_DAY) <= 14) {
            cbHora.addItem(formatoHora.format(hora.getTime()));
            hora.add(Calendar.MINUTE, 30);
        }
    }
