    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(medico!=null && paciente != null){
            especialidad = new Especialidad();
            especialidad.setNombre(cbEspecialidad.getSelectedItem().toString());
            datosCita();
            this.dispose();
            padre.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "Completa todos los campos"
                    ,"Agregar Cita", JOptionPane.INFORMATION_MESSAGE);
        
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void datosCita() {
        Cita cita = new Cita();
        cita.setMedico(medico);
        cita.setPaciente(paciente);
        cita.setFecha(cbFecha.getSelectedItem().toString());
        cita.setHora(cbHora.getSelectedItem().toString());
        cita.setEspecialida(especialidad);
        padre.addCita(cita);
    }

